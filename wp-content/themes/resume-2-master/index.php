    $message = $conn->real_escape_string($_POST['message']);

    // Check if a message from this email already exists
    $checkQuery = "SELECT * FROM messages WHERE email = '$email'";
    $result = $conn->query($checkQuery);

    if ($result->num_rows > 0) {
        // If a message already exists from this email, set error message
        $msg = "A message has already been sent with this email.";
    } else {
        // If no message from this email, insert new message
